var x=90;
if(0<=x && x<50)
{
    document.write("Failed");
}
 else if(50<=x && x<60)
{
    document.write("D");
}
else if(60<=x && x<70)
{
    document.write("C");
}
else if(70<=x && x<80)
{
    document.write("B");
}
else if(80<=x && x<90)
{
    document.write("A");
}
else if(90<=x && x<=100)
{
    document.write("A+");
}
else {document.write("Invalid mark");}